def crop_roi_safe(image, bbox):
    h, w, _ = image.shape
    x1, y1, x2, y2 = bbox

    # Clamp safely
    x1 = max(0, min(w, x1))
    x2 = max(0, min(w, x2))
    y1 = max(0, min(h, y1))
    y2 = max(0, min(h, y2))

    if x2 <= x1 or y2 <= y1:
        return None

    return image[y1:y2, x1:x2]

def build_roi_dataset(df, image_folder):

    images = []
    labels = []
    groups = []

    for _, row in df.iterrows():

        patient_id = row['PATIENT_ID']
        hb = row['HB_LEVEL_GperL']

        img_path = os.path.join(image_folder, f"{str(patient_id)}.jpg")

        image = cv2.imread(img_path)

        if image is None:
            print(f"Warning: Missing image {img_path}")
            continue

        image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
        # Correct orientation to match annotation
        image = cv2.rotate(image, cv2.ROTATE_90_CLOCKWISE)
        image = cv2.flip(image, 1)


        nail_boxes = ast.literal_eval(row['NAIL_BOUNDING_BOXES'])

        for box in nail_boxes:

            crop = crop_roi_safe(image, box)

            if crop is None or crop.size == 0:
                continue

            crop = cv2.resize(crop, (IMAGE_SIZE, IMAGE_SIZE))
            crop = crop / 255.0

            images.append(crop)
            labels.append(hb)
            groups.append(patient_id)

    return np.array(images), np.array(labels), np.array(groups)

